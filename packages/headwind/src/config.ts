// Lazy-loaded config to avoid top-level await (enables bun --compile)
let _config: HeadwindConfig | null = null

export async function getConfig(): Promise<HeadwindConfig> {
  if (!_config) {
    _config = await loadConfig({
  name: 'headwind',
  defaultConfig,
})
  }
  return _config
}

